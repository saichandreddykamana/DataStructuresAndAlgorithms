# Selection Sort

def selection_sort(arr):
    for i in range(0, len(arr)):
        index = get_least_index(i, arr)
        if arr[index] < arr[i]:
            temp = arr[index]
            arr[index] = arr[i]
            arr[i] = temp
    return arr

def get_least_index(start, arr):
    least_index = start
    for i in range(start, len(arr)):
        if arr[least_index] > arr[i]:
            least_index = i
    return least_index
    
print(selection_sort([17, 31, 39, 39, 42, 15, 25, 44, 1, 35, 27, 49, 47]))

